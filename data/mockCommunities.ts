// Helper function to get community by slug
export function getCommunityBySlug(slug: string): Community | undefined {
  return mockCommunities.find(community => community.slug === slug);
}

// Helper function to get related communities (same category, excluding current)
export function getRelatedCommunities(currentSlug: string, limit: number = 3): Community[] {
  const currentCommunity = getCommunityBySlug(currentSlug);
  if (!currentCommunity) return [];
  
  return mockCommunities
    .filter(community => 
      community.slug !== currentSlug && 
      community.category_id === currentCommunity.category_id
    )
    .slice(0, limit);
}

// Helper function to format members count
export function formatMembersCount(count: number): string {
  if (count >= 1000) {
    return `${(count / 1000).toFixed(1)}K`;
  }
  return count.toString();
}
